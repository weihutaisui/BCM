#ifndef XCHG_ASSERT_CFG_CUSTOM_H
#define XCHG_ASSERT_CFG_CUSTOM_H

/*
 * The version of this file which exists in the /cfginc
 * directory should be EMPTY.
 *
 * ALL customizations should go into a copy of this file located in the
 * appropriate build directory.
 *
 * The prescence of a xchgAssertCfgCustom.h file in the build directory
 * precludes the inclusion of the same file from the inc directory.
 *
 * The empty versions of the files are provided so that if no defaults need
 * to be overridden, then no copy of the file has to exist in the build
 * directory.
 *
 * ALL default values for options should be located in:
 *
 *    /cfginc/xchgAssertCfg.h
 *
 * This comment block may be removed from copies of this file located in
 * a build directory.
 */

 #endif   /* XCHG_ASSERT_CFG_CUSTOM_H */
